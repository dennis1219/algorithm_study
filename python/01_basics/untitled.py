import sys
import itertools
import math
import statistics

a = sys.stdin.readline().rstrip()
b,c = map(int, sys.stdin.readline().rstrip().split())

l = list()

for i in range(a):
    while True:
        statistics.multimode()
        statistics.median()
        itertools.permutations()
        itertools.combinations()
        itertools.combinations_with_replacement()

print(l + a + b + c)

